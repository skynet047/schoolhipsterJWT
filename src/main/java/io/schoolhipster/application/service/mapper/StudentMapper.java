package io.schoolhipster.application.service.mapper;

import io.schoolhipster.application.domain.*;
import io.schoolhipster.application.service.dto.StudentDTO;

import org.mapstruct.*;

/**
 * Mapper for the entity Student and its DTO StudentDTO.
 */
@Mapper(componentModel = "spring", uses = {LessonMapper.class})
public interface StudentMapper extends EntityMapper<StudentDTO, Student> {



    default Student fromId(Long id) {
        if (id == null) {
            return null;
        }
        Student student = new Student();
        student.setId(id);
        return student;
    }
}
